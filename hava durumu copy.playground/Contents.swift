import UIKit

let weather = "rain"                //Setting the weather as rain

if weather == "rain" {                  
    print("Şemsiye almalısınız.")              //If the weather is rain, prints this
} else if weather == "snow" {
    print("Atkı almalısınız.")                //If it is snow, prints this
} else if weather == "sunny" {                
    print("Güneş gözlüğü almalısınız.")            //If it is sunny, prints this
} else {
    print("Özel bir tavsiyem yok.")                //Else, prints this
}
